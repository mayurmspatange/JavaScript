// Assignment Operators

// Assign the value 5 to x
let x;
x = 5;
// Assign the value 2 to y
let y = 2;
// Assign the value x + y to z:
let z = x + y;
console.log("value of x:",  x , "value of y" , y ," value of z", z);

z += y  // z = z + y 

console.log("value of y" , y ," value of z", z);


z -= y  // z = z - y 
console.log("value of y" , y ," value of z", z);


z *= y  // z = z * y 
console.log("value of y" , y ," value of z", z);



z /= y  // z = z / y 
console.log("value of y" , y ," value of z", z);


z %= y  // z = z % y 
console.log("value of y" , y ," value of z", z);


console.log("value of x" , x ," value of z", y);

